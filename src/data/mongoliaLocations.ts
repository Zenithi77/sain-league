/** Sorted list of all аймаг/хот names */
export const aimagList = Object.keys(mongoliaLocations).sort();

/** Get sorted list of сум/дүүрэг for a given аймаг/хот */
export function getSumDuureg(aimag: string): string[] {
  return Object.keys(mongoliaLocations[aimag] ?? {}).sort();
}

/** Get sorted list of хороо/баг for a given аймаг + сум/дүүрэг */
export function getKhorooBag(aimag: string, sumDuureg: string): string[] {
  return (mongoliaLocations[aimag]?.[sumDuureg] ?? []).slice().sort();
}
